#include "../libft.h"
#include <stdlib.h>
#include <string.h>

char			*ft_strdup(const char *s1)
{
	char	*copy;
	int		len;
	int		i;

	i = 0;
	len = ft_strlen(s1);
	copy = malloc(sizeof(char) * (1 + len));
	if (copy == NULL)
		return (0);
	while (s1[i] != '\0')
	{
		copy[i] = s1[i];
		i++;
	}
	copy[len] = '\0';
	return (copy);
}
